    private void jButtontimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtontimeActionPerformed
        float speed_meter = 0;
        float distance_meter = 0;
//       Speed convert kilometer to meter
        if (jRadioButtonSpeedKiloMeter.isSelected()) {
            String speed_value = jTextFieldSpeed.getText();
            float speed_km = Float.valueOf(speed_value);
            speed_meter = speed_km * 1000;
        } else {
            String speed_value = jTextFieldSpeed.getText();
            speed_meter = Float.valueOf(speed_value);
        }

//       Distance convert kilomter to meter
        if (jRadioButtonDistenceKiloMeter.isSelected()) {
            String distance_value = JTextFieldDistance.getText();
            float distance_km = Float.valueOf(distance_value);
            distance_meter = distance_km * 1000;
        } else {
            String distance_value = JTextFieldDistance.getText();
            distance_meter = Float.valueOf(distance_value);
        }

        float time = distance_meter / speed_meter;

        if (jRadioButtonHours.isSelected()) {
            time = time / 3600;
            jTextFieldTime.setText(String.valueOf(time + "House"));
        } else if (jRadioButtonMinute.isSelected()) {
            time = (time / 3600) * 60;
            jTextFieldTime.setText(String.valueOf(time + "Minutes"));
        } else {
            jTextFieldTime.setText(String.valueOf(time + "second"));
        }


    }//GEN-LAST:event_jButtontimeActionPerformed

