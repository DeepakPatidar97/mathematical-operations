    private void jButtonSpeedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSpeedActionPerformed
        // TODO add your handling code here:
        float time_second = 0;
        String time_value = jTextFieldTime.getText();
        float time = Float.valueOf(time_value);
        if (jRadioButtonHours.isSelected()) {
            time_second = (time * 60) * 60;
        } else if (jRadioButtonMinute.isSelected()) {
            time_second = time * 60;
        } else {
            time_second = time;
        }

//       Distance convert kilomter to meter
        float distance_meter = 0;
        String distance_value = JTextFieldDistance.getText();
        float distance = Float.valueOf(distance_value);
        if (jRadioButtonDistenceKiloMeter.isSelected()) {
            distance_meter = distance * 1000;
        } else {
            distance_meter = distance;
        }

        float speed = distance / time;

        float speed_KM = 0;

        if (jRadioButtonSpeedKiloMeter.isSelected()) {
            speed_KM = speed / 1000;
            jTextFieldSpeed.setText(String.valueOf(speed_KM + "KM"));
        } else {
            jTextFieldSpeed.setText(String.valueOf(speed_KM + "Meter"));
        }
    }//GEN-LAST:event_jButtonSpeedActionPerformed

